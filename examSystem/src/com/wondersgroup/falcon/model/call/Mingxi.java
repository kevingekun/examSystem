package com.wondersgroup.falcon.model.call;

import java.util.Date;

public class Mingxi {

	private String pingfenid;

	private String kaoheid;

	private String mark;

	private String id;
	
	private Date riqi;

	public String getKaoheid() {
		return kaoheid;
	}

	public void setKaoheid(String kaoheid) {
		this.kaoheid = kaoheid;
	}

	public String getMark() {
		return mark;
	}

	public void setMark(String mark) {
		this.mark = mark;
	}

	public String getPingfenid() {
		return pingfenid;
	}

	public void setPingfenid(String pingfenid) {
		this.pingfenid = pingfenid;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Date getRiqi() {
		return riqi;
	}

	public void setRiqi(Date riqi) {
		this.riqi = riqi;
	}

}
